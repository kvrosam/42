#include <unistd.h>
#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	return i;
}

int	main(void)
{
	char	str[] = "hello";
	
	printf("\ni= %d", ft_strlen(str));
	return (0);
}
